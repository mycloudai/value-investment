    def insert_after_title(m):
        return m.group(0) + f'summary: "{summary}"\n'
    
    new_content = re.sub(r'^(title:.*\n)', insert_after_title, content, count=1, flags=re.MULTILINE)
    
    if new_content == content:
        print(f"  WARN (no title found): {filepath}")
        return False
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(new_content)
    
    return True


